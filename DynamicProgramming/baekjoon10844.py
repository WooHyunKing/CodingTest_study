# [1]0, [1]2, [2]1, [2]3, [3]2, [3]4, [4]3, [4]5, [5]4, [5]6, [6]5, [6]7, [7]6, [7]8, [8]7, [8]9, [9]8

# [10]1, [12]1, [12]3, [21]0, [21]2, [23]2, [23]4, [32]1, [32]3, [34]3, [34]5, [43]2, [43]5

dp = [[0]*10 for i in range(101)]

for i in range(1,10):
    dp[0][i] = 1

for i in range(n):
    for j in range(10):
        if j == 0:
            dp[i+1][j+1] += dp[i][j]
        elif j == 9:
            dp[i+1][j-1] += dp[i][j]
        else:
            dp[i+1][j+1] += dp[i][j]
            dp[i+1][j-1] += dp[i][j]

print(sum(dp[n-1])%1000000000)

